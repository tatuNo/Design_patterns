package mcdonalds;

import java.util.ArrayList;
import java.util.List;


// mc donaldsin purilainen
public class BigMac {
	
	private List <String> ainekset = new ArrayList<String>();
	
	public void setJuusto(Juusto juusto) {
		ainekset.add(juusto.toString());
	}
	
	public void setMajoneesi(Majoneesi majoneesi) {
		ainekset.add(majoneesi.toString());
	}
	
	public void setPihvi(Pihvi pihvi) {
		ainekset.add(pihvi.toString());
	}
	
	public void setSalaatti(Salaatti salaatti) {
		ainekset.add(salaatti.toString());
	}
	
	public void setSampyla(Sampyla sampyla) {
		ainekset.add(sampyla.toString());
	}
	
	@Override
	public String toString() {
		return String.join(",", ainekset);
	}	
}

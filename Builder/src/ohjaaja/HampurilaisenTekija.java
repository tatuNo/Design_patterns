package ohjaaja;

import hampurilaisbuilder.HampurilaisBuilder;

public class HampurilaisenTekija {
	
	private HampurilaisBuilder hampurilaisbuilder;
	
	public void setRavintola(HampurilaisBuilder ravintola) {
		this.hampurilaisbuilder = ravintola;
	}
	
	public void teeHampurilainen () {
		hampurilaisbuilder.luoUusiHampurilainen();
		hampurilaisbuilder.lisaaJuusto();
		hampurilaisbuilder.lisaaMajoneesi();
		hampurilaisbuilder.lisaaPihvit();
		hampurilaisbuilder.lisaaSalaatti();
		hampurilaisbuilder.lisaaSampylat();
	}

}
